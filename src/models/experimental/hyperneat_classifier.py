"""
Novel Model Family 1: HyperNEAT-Inspired Topology Search
==========================================================
Implements a CPPN (Compositional Pattern Producing Network)-based approach
where the neural network *weights* are generated by a function of neuron
geometry, rather than learned directly.

The key idea: instead of learning W[i,j] directly, we learn a CPPN f(x_i, y_i, x_j, y_j)
that generates W[i,j] as a function of neuron positions. This allows the model to
discover *geometric patterns* in the matchup feature space — e.g., "features in the
left half of the grid interact with features in the right half" — without us specifying
which pairs of features interact.

Implementation:
  - Lay features on a 1D grid by conceptual proximity (Elo features left, squad right)
  - CPPN: small MLP that maps (x_i, x_j) → weight
  - Evolve CPPN parameters using a genetic algorithm (DEAP)
  - Fitness: LOTO-CV log-loss
"""

